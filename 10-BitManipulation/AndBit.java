public class AndBit {
    public static void main(String[] args) {
        System.out.println("---------------------AND Bit -----------------------------");
        System.out.println( 5 & 6);

        // OR Bit 
        System.out.println("---------------------OR Bit -----------------------------");
        System.out.println(5 | 6);

        // XOR BIT

        System.out.println("---------------------XOR Bit -----------------------------");
        System.out.println(5^6);
    }
}
