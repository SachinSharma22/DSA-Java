public class RelationalOperators {
    public static void main(String[] args) {
        /*Relational Operators --> ==, !=,>,<,>=, <= */

        int a = 10;
        int b = 101;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
