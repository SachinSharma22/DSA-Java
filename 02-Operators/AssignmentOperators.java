public class AssignmentOperators {
    public static void main(String[] args) {
        /*Assignment Operators -> =, +=, -=, *=, /= 
        a = a+10 == a += 10*/

        int a = 10;
        System.out.println(a);

        a += 10;
        System.out.println(a);

        a -= 10;
        System.out.println(a);

        a *= 10;
        System.out.println(a);

        a /= 10;
        System.out.println(a);
    }
}
