public class LogicalOperators {
    public static void main(String[] args) {
        /*Logical Operators --> &&, ||, ! 
        1. && - Logical AND
        2. || - Logical OR
        3. ! - Logical NOT*/
        
        System.out.println((3<4)&&(4<5));
        System.out.println((3>4) || (3>2));
        System.out.println(!(3>2));
    }
}
