public class DataTypes {
    public static void main(String[] args) {
        // Primitive and non-premitive data types
        // Premitive DataTypes ---> Byte, Short, Char, Boolean, Int, Long, Float, Double
        // Non-Premitive DataTypes --> String, Array, Class, Object, interface
        
        byte num = 8;
        System.out.println(num);

        char ch = 'a';
        System.out.println(ch);

        boolean var = false;
        System.out.println(var);

        float n = 9.9f;
        System.out.println(n);

        int i = 1;
        System.out.println(i);

        // Sum of two numbers
        int a = 3;
        int b = 4;
        int sum = a + b;
        System.out.println("Sum of A + B = "+ sum);

        // Comments in JAVA -- Single line comment
        /* MultiLine Comment
        We can comment multiple line at once using this comment */
    }
}
