public class Variables{
   public static void main(String[] args) {
    int a = 10;
    int b = 20;
    System.out.println(a);
    System.out.println(b);

    // we can reinitialize any valiables in java

    a = 11;
    System.out.println(a);

    String name = "Keshav";
    System.out.println(name);
   } 
}